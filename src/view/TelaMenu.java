    public void listar() {

        NotaController nota = new NotaController();
        List<Aluno> lista = nota.listarNotas(usuarioLogado);

        DefaultTableModel dados = (DefaultTableModel) tabelaNotas.getModel();

        dados.setNumRows(0);

        for (Aluno a : lista) {
            dados.addRow(new Object[]{
                a.getDisciplina(),
                a.getNota(),
                a.getFaltas(),});
        }
    }


